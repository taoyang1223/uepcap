// setToSlice converts a set to a sorted slice
func setToSlice(set map[string]bool) []string {
	if len(set) == 0 {
		return nil
	}
	result := make([]string, 0, len(set))
	for k := range set {
		result = append(result, k)
	}
	sort.Strings(result)
	return result
}

// parseEpochTime parses epoch time string to float64 seconds
func parseEpochTime(epochStr string) (float64, error) {
	// Format: "1703001234.123456789"
	return strconv.ParseFloat(epochStr, 64)
}

// isValidIP checks if a string looks like an IP address
func isValidIP(s string) bool {
	// Simple check: contains dots and only digits/dots (IPv4) or colons (IPv6)
	if strings.Contains(s, ".") {
		parts := strings.Split(s, ".")
		if len(parts) == 4 {
			for _, p := range parts {
				if _, err := strconv.Atoi(p); err != nil {
					return false
				}
			}
			return true
		}
	}
	if strings.Contains(s, ":") {
		// IPv6-like
		return true
	}
	return false
}

// GenerateFlow handles POST /api/jobs/{id}/flow/generate
func (h *Handler) GenerateFlow(w http.ResponseWriter, r *http.Request) {
	id := r.PathValue("id")
	job, ok := h.jobMgr.GetJob(id)
	if !ok {
		writeError(w, http.StatusNotFound, "job not found")
		return
	}

	var req FlowGenerateRequest
	if err := json.NewDecoder(r.Body).Decode(&req); err != nil {
		writeError(w, http.StatusBadRequest, fmt.Sprintf("invalid request body: %v", err))
		return
	}

	if req.Filter == "" {
		writeError(w, http.StatusBadRequest, "filter is required")
		return
	}

	maxPackets := req.MaxEvents
	if maxPackets <= 0 {
		maxPackets = 1000 // default
	}

	// Get cached text or generate new (same data source as "复制JSON" button)
	cacheKey := generateTextCacheKey(req.Filter)
	var compactJSON string
	if cached, ok := h.jobMgr.GetCachedTextExport(id, cacheKey); ok {
		compactJSON = cached
	} else {
		ctx, cancel := context.WithTimeout(context.Background(), 2*time.Minute)
		defer cancel()

		result, err := tshark.TsharkCompactJSON(ctx, job.MergedPcap, req.Filter, applicationProtocols)
		if err != nil {
			writeError(w, http.StatusInternalServerError, fmt.Sprintf("failed to get packets: %v", err))
			return
		}
		if result.ExitCode != 0 {
			writeError(w, http.StatusInternalServerError, fmt.Sprintf("tshark error: %s", strings.TrimSpace(result.Stderr)))
			return
		}

		var err2 error
		compactJSON, err2 = packet.SimplifyPacketsJSON(result.Stdout)
		if err2 != nil {
			compactJSON = result.Stdout
		}
		h.jobMgr.CacheTextExport(id, cacheKey, compactJSON)
	}

	// Limit the number of packets sent to LLM if needed
	limitedJSON, _, err := limitPacketsJSON(compactJSON, maxPackets)
	if err != nil {
		writeError(w, http.StatusInternalServerError, fmt.Sprintf("failed to process packets: %v", err))
		return
	}

	// Extract key params from compactJSON for response
	keyParams := extractKeyParamsFromJSON(compactJSON)

	// Extract packet columns using tshark (Protocol, Info, etc.)
	columnsCtx, columnsCancel := context.WithTimeout(context.Background(), 30*time.Second)
	packetColumns, err := tshark.TsharkPacketColumns(columnsCtx, job.MergedPcap, req.Filter)
	columnsCancel()
	if err != nil {
		log.Printf("[FlowGenerate] Warning: failed to extract packet columns: %v", err)
		packetColumns = nil // Continue without columns
	}

	// Filter heartbeat packets to make LLM input deterministic
	llmInputJSON, removedHeartbeats, err := filterHeartbeatPacketsJSON(limitedJSON)
	if err != nil {
		llmInputJSON = limitedJSON
		removedHeartbeats = 0
	}

	// Count packets in llmInputJSON and build valid frame numbers set
	var packets []map[string]interface{}
	if err := json.Unmarshal([]byte(llmInputJSON), &packets); err != nil {
		writeError(w, http.StatusInternalServerError, fmt.Sprintf("failed to parse packets: %v", err))
		return
	}
	packetCount := len(packets)

	// Build valid frame numbers set
	validFrameNumbers := make(map[string]bool)
	for _, pkt := range packets {
		if frame, ok := pkt["frame"].(map[string]interface{}); ok {
			if num, ok := frame["number"].(string); ok && num != "" {
				validFrameNumbers[num] = true
			}
		}
	}

	log.Printf("[FlowGenerate] Job %s: %d packets after filtering (removed %d heartbeats)", id, packetCount, removedHeartbeats)

	// Use deterministic rule-based IP→NE inference (no LLM)
	annotations := InferIPMappingsWithEmptyStages(llmInputJSON)

	// No stage-based filtering - use all packets
	renderJSON := llmInputJSON
	flowName := "信令流程图"
	if annotations != nil && annotations.FlowName != "" {
		flowName = annotations.FlowName
	}

	// Generate Mermaid deterministically from Go using annotations and packet columns
	mermaid := buildDeterministicMermaid(renderJSON, flowName, annotations, packetColumns)

	// Build details mapping (guaranteed 1:1 with Mermaid)
	details := buildDetailsMapping(renderJSON)

	log.Printf("[FlowGenerate] Generated Mermaid with %d messages, annotations=%v", len(details), annotations != nil)

	writeSuccess(w, &FlowGenerateResponse{
		FlowName:      flowName,
		Mermaid:       mermaid,
		KeyParams:     keyParams,
		Details:       details,
		InputJSON:     renderJSON, // Return the subset JSON that matches the diagram
		Annotations:   annotations,
		PacketColumns: packetColumns,
	})
}

// limitPacketsJSON limits the number of packets in JSON array
func limitPacketsJSON(compactJSON string, maxPackets int) (string, int, error) {
	var packets []map[string]interface{}
	if err := json.Unmarshal([]byte(compactJSON), &packets); err != nil {
		return "", 0, fmt.Errorf("failed to parse JSON: %w", err)
	}

	if len(packets) > maxPackets {
		packets = packets[:maxPackets]
	}

	result, err := json.Marshal(packets)
	if err != nil {
		return "", 0, fmt.Errorf("failed to marshal limited packets: %w", err)
	}

	return string(result), len(packets), nil
}

