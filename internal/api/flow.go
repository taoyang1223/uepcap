// FlowBriefRequest represents the request for flow brief
type FlowBriefRequest struct {
	Filter string `json:"filter"`
}

// FlowBriefResponse represents the brief summary of filtered packets
type FlowBriefResponse struct {
	PacketCount      int                  `json:"packet_count"`
	TimeRange        *TimeRange           `json:"time_range,omitempty"`
	ProtocolsPresent []string             `json:"protocols_present"`
	KeyParams        *KeyParamsCandidates `json:"key_params"`
}

// TimeRange represents the time span of packets
type TimeRange struct {
	Start         string  `json:"start"`          // absolute time
	End           string  `json:"end"`            // absolute time
	DurationMs    float64 `json:"duration_ms"`    // duration in milliseconds
	StartRelative string  `json:"start_relative"` // relative time
	EndRelative   string  `json:"end_relative"`   // relative time
}

// KeyParamsCandidates holds key parameters extracted from packets
type KeyParamsCandidates struct {
	IMSI        []string `json:"imsi,omitempty"`
	SUPI        []string `json:"supi,omitempty"`
	UEIP        []string `json:"ue_ip,omitempty"`
	TEID        []string `json:"teid,omitempty"`
	SEID        []string `json:"seid,omitempty"`
	QFI         []string `json:"qfi,omitempty"`
	RanUeNgapID []string `json:"ran_ue_ngap_id,omitempty"`
	AmfUeNgapID []string `json:"amf_ue_ngap_id,omitempty"`
}

// FlowGenerateRequest represents the request for flow generation
type FlowGenerateRequest struct {
	Filter    string `json:"filter"`
	MaxEvents int    `json:"max_events,omitempty"` // max events to send to LLM (default 200)
}

// FlowGenerateResponse represents the response with Mermaid diagram
type FlowGenerateResponse struct {
	FlowName  string               `json:"flow_name"`
	Mermaid   string               `json:"mermaid"`
	KeyParams *KeyParamsCandidates `json:"key_params,omitempty"`
	// Details is the mapping from Mermaid autonumber index to original packet frame.number.
	// Frontend uses this to correlate diagram messages with input packets.
	Details []FlowMessageDetail `json:"details,omitempty"`
	// InputJSON is the original compact packet JSON sent to LLM (for frontend to derive rich details).
	InputJSON string `json:"input_json,omitempty"`
	// Annotations contains LLM-provided IP→NE mapping and stage segmentation.
	// May be nil if LLM parsing failed (diagram still generated deterministically by Go).
	Annotations *FlowAnnotationsV1 `json:"annotations,omitempty"`
	// PacketColumns contains tshark-extracted column info (Protocol, Info, etc.) for each frame.
	// Used by frontend to display message labels and packet details without LLM parsing.
	PacketColumns map[string]*tshark.PacketColumns `json:"packet_columns,omitempty"`
}

// FlowMessageDetail maps a Mermaid message (by autonumber index) to the original packet.
// idx: 1-based message order in the Mermaid diagram (matches autonumber order).
// frame: 1-based input packet index (position in the input JSON array).
// originNumber: the original capture frame.number from pcap (Wireshark frame number).
type FlowMessageDetail struct {
	Idx          int    `json:"idx"`
	Frame        int    `json:"frame,omitempty"`
	OriginNumber string `json:"originNumber,omitempty"` // original pcap frame.number
}

// FlowFinalEvent is sent to the frontend via SSE after the LLM stream completes.
// It guarantees that the Mermaid and details_map are consistent with the exact input_json
// that was sent to the LLM (and sent to the web).
type FlowFinalEvent struct {
	FlowName string               `json:"flow_name"`
	Mermaid  string               `json:"mermaid"`
	Details  []FlowMessageDetail  `json:"details_map"`
	Mode     string               `json:"mode"` // "go_deterministic" (always now)
	Reasons  []string             `json:"reasons,omitempty"`
	Stats    *FlowValidationStats `json:"stats,omitempty"`
	// Annotations contains LLM-provided IP→NE mapping and stage segmentation.
	// May be nil if LLM parsing failed.
	Annotations *FlowAnnotationsV1 `json:"annotations,omitempty"`
	// PacketColumns contains tshark-extracted column info (Protocol, Info, etc.) for each frame.
	// Used by frontend to display message labels and packet details without LLM parsing.
	PacketColumns map[string]*tshark.PacketColumns `json:"packet_columns,omitempty"`
}

type FlowValidationStats struct {
	InputPackets      int `json:"input_packets"`
	MermaidMessages   int `json:"mermaid_messages"`
	MappingEntries    int `json:"mapping_entries"`
	RemovedHeartbeats int `json:"removed_heartbeats"`
}

// FlowAnnotationsV1 is the structured JSON returned by LLM for IP→NE mapping and stage segmentation.
// The Mermaid diagram is always generated by Go; LLM only provides these annotations.
type FlowAnnotationsV1 struct {
	Version  string      `json:"version"` // should be "flow_annotations_v1"
	FlowName string      `json:"flow_name,omitempty"`
	IPMap    []IPMapping `json:"ip_map"`
	Stages   []FlowStage `json:"stages"`
}

// IPMapping maps an IP address to a network element role.
type IPMapping struct {
	IP         string  `json:"ip"`
	NE         string  `json:"ne"`                   // gNB, AMF, SMF, UPF, SGW, PGW, Unknown, etc.
	Confidence float64 `json:"confidence,omitempty"` // 0.0-1.0, optional
	Reason     string  `json:"reason,omitempty"`     // optional explanation
}

// FlowStage represents a logical stage/phase in the signaling flow.
// Each stage contains an explicit list of frames (packets) that belong to it.
type FlowStage struct {
	Name       string           `json:"name"`
	Frames     []FlowStageFrame `json:"frames"` // explicit list of frames in this stage
	Summary    string           `json:"summary,omitempty"`
	Confidence float64          `json:"confidence,omitempty"`
}

// FlowStageFrame represents a single frame within a stage.
// Number corresponds to frame.number from input JSON (Wireshark frame number).
type FlowStageFrame struct {
	Number string `json:"number"`          // frame.number from input JSON
	Title  string `json:"title,omitempty"` // message name for Mermaid label
	Note   string `json:"note,omitempty"`  // operation/effect description for Mermaid Note
}

// GetFlowBrief handles POST /api/jobs/{id}/flow/brief
func (h *Handler) GetFlowBrief(w http.ResponseWriter, r *http.Request) {
	id := r.PathValue("id")
	job, ok := h.jobMgr.GetJob(id)
	if !ok {
		writeError(w, http.StatusNotFound, "job not found")
		return
	}

	var req FlowBriefRequest
	if err := json.NewDecoder(r.Body).Decode(&req); err != nil {
		writeError(w, http.StatusBadRequest, fmt.Sprintf("invalid request body: %v", err))
		return
	}

	if req.Filter == "" {
		writeError(w, http.StatusBadRequest, "filter is required")
		return
	}

	// Get cached text or generate new
	cacheKey := generateTextCacheKey(req.Filter)
	var compactJSON string
	if cached, ok := h.jobMgr.GetCachedTextExport(id, cacheKey); ok {
		compactJSON = cached
	} else {
		ctx, cancel := context.WithTimeout(context.Background(), 2*time.Minute)
		defer cancel()

		result, err := tshark.TsharkCompactJSON(ctx, job.MergedPcap, req.Filter, applicationProtocols)
		if err != nil {
			writeError(w, http.StatusInternalServerError, fmt.Sprintf("failed to get packets: %v", err))
			return
		}
		if result.ExitCode != 0 {
			writeError(w, http.StatusInternalServerError, fmt.Sprintf("tshark error: %s", strings.TrimSpace(result.Stderr)))
			return
		}

		compactJSON, err = packet.SimplifyPacketsJSON(result.Stdout)
		if err != nil {
			compactJSON = result.Stdout
		}
		h.jobMgr.CacheTextExport(id, cacheKey, compactJSON)
	}

	// Parse and analyze the packets
	brief, err := analyzePacketsForBrief(compactJSON)
	if err != nil {
		writeError(w, http.StatusInternalServerError, fmt.Sprintf("failed to analyze packets: %v", err))
		return
	}

	writeSuccess(w, brief)
}

// analyzePacketsForBrief extracts summary info from compact JSON
func analyzePacketsForBrief(compactJSON string) (*FlowBriefResponse, error) {
	var packets []map[string]interface{}
	if err := json.Unmarshal([]byte(compactJSON), &packets); err != nil {
		return nil, fmt.Errorf("failed to parse JSON: %w", err)
	}

	brief := &FlowBriefResponse{
		PacketCount:      len(packets),
		ProtocolsPresent: []string{},
		KeyParams:        &KeyParamsCandidates{},
	}

	protocolSet := make(map[string]bool)
	imsiSet := make(map[string]bool)
	supiSet := make(map[string]bool)
	ueipSet := make(map[string]bool)
	teidSet := make(map[string]bool)
	seidSet := make(map[string]bool)
	qfiSet := make(map[string]bool)
	ranIdSet := make(map[string]bool)
	amfIdSet := make(map[string]bool)

	var firstTime, lastTime string
	var firstRelative, lastRelative string

	for _, pkt := range packets {
		// Extract frame info
		if frame, ok := pkt["frame"].(map[string]interface{}); ok {
			if t, ok := frame["time_absolute"].(string); ok && t != "" {
				if firstTime == "" {
					firstTime = t
				}
				lastTime = t
			}
			if t, ok := frame["time"].(string); ok && t != "" {
				if firstRelative == "" {
					firstRelative = t
				}
				lastRelative = t
			}
		}

		// Extract application layer protocols and params
		if app, ok := pkt["application"].(map[string]interface{}); ok {
			for proto, info := range app {
				protocolSet[proto] = true
				if infoMap, ok := info.(map[string]interface{}); ok {
					extractKeyParams(proto, infoMap, imsiSet, supiSet, ueipSet, teidSet, seidSet, qfiSet, ranIdSet, amfIdSet)
				}
			}
		}

		// Also check layers for IPs that might be UE IPs
		if layers, ok := pkt["layers"].(map[string]interface{}); ok {
			// We'll collect src/dst IPs but filter later for UE candidates
			// For now, just note them
			_ = layers
		}
	}

	// Build protocol list
	for proto := range protocolSet {
		brief.ProtocolsPresent = append(brief.ProtocolsPresent, proto)
	}
	sort.Strings(brief.ProtocolsPresent)

	// Build key params
	brief.KeyParams.IMSI = setToSlice(imsiSet)
	brief.KeyParams.SUPI = setToSlice(supiSet)
	brief.KeyParams.UEIP = setToSlice(ueipSet)
	brief.KeyParams.TEID = setToSlice(teidSet)
	brief.KeyParams.SEID = setToSlice(seidSet)
	brief.KeyParams.QFI = setToSlice(qfiSet)
	brief.KeyParams.RanUeNgapID = setToSlice(ranIdSet)
	brief.KeyParams.AmfUeNgapID = setToSlice(amfIdSet)

	// Calculate time range
	if firstTime != "" && lastTime != "" {
		startSec, _ := parseEpochTime(firstTime)
		endSec, _ := parseEpochTime(lastTime)
		durationMs := (endSec - startSec) * 1000

		brief.TimeRange = &TimeRange{
			Start:         firstTime,
			End:           lastTime,
			DurationMs:    durationMs,
			StartRelative: firstRelative,
			EndRelative:   lastRelative,
		}
	}

	return brief, nil
}

// extractKeyParams extracts key parameters from protocol info
func extractKeyParams(proto string, info map[string]interface{}, imsiSet, supiSet, ueipSet, teidSet, seidSet, qfiSet, ranIdSet, amfIdSet map[string]bool) {
	// Helper to add string to set
	addToSet := func(set map[string]bool, key string) {
		if v, ok := info[key].(string); ok && v != "" {
			set[v] = true
		}
	}

	switch proto {
	case "ngap":
		// Look for NAS info nested in ngap
		if nas, ok := info["nas"].(map[string]interface{}); ok {
			if msin, ok := nas["msin"].(string); ok && msin != "" {
				// Reconstruct IMSI from mcc+mnc+msin if available
				mcc, _ := nas["mcc"].(string)
				mnc, _ := nas["mnc"].(string)
				if mcc != "" && mnc != "" {
					imsiSet[mcc+mnc+msin] = true
				} else {
					imsiSet[msin] = true
				}
			}
			if supi, ok := nas["supi"].(string); ok && supi != "" {
				supiSet[supi] = true
			}
		}
		// Look in ies for IDs
		if ies, ok := info["ies"].(map[string]interface{}); ok {
			extractFromIEs(ies, ranIdSet, amfIdSet, ueipSet, teidSet, seidSet, qfiSet)
		}

	case "nas-5gs":
		addToSet(supiSet, "supi")
		if msin, ok := info["msin"].(string); ok && msin != "" {
			mcc, _ := info["mcc"].(string)
			mnc, _ := info["mnc"].(string)
			if mcc != "" && mnc != "" {
				imsiSet[mcc+mnc+msin] = true
			}
		}

	case "pfcp":
		addToSet(seidSet, "seid")
		if ies, ok := info["ies"].(map[string]interface{}); ok {
			extractPFCPParams(ies, ueipSet, teidSet, qfiSet, seidSet)
		}

	case "gtpv2":
		addToSet(teidSet, "teid")
		if ies, ok := info["ies"].(map[string]interface{}); ok {
			extractGTPv2Params(ies, imsiSet, teidSet, ueipSet)
		}

	case "gtp":
		addToSet(teidSet, "teid")
		if ies, ok := info["ies"].(map[string]interface{}); ok {
			extractGTPUParams(ies, qfiSet)
		}

	case "s1ap":
		if ies, ok := info["ies"].(map[string]interface{}); ok {
			extractS1APParams(ies, imsiSet, ueipSet)
		}
	}
}

// extractFromIEs recursively searches for key values in IE structures
func extractFromIEs(ies map[string]interface{}, ranIdSet, amfIdSet, ueipSet, teidSet, seidSet, qfiSet map[string]bool) {
	var search func(data interface{})
	search = func(data interface{}) {
		switch v := data.(type) {
		case map[string]interface{}:
			for key, val := range v {
				keyLower := strings.ToLower(key)
				if strings.Contains(keyLower, "ran-ue-ngap-id") || strings.Contains(keyLower, "ran_ue_ngap_id") {
					if s, ok := val.(string); ok && s != "" {
						ranIdSet[s] = true
					}
				}
				if strings.Contains(keyLower, "amf-ue-ngap-id") || strings.Contains(keyLower, "amf_ue_ngap_id") {
					if s, ok := val.(string); ok && s != "" {
						amfIdSet[s] = true
					}
				}
				if strings.Contains(keyLower, "pdu_address") || strings.Contains(keyLower, "ue_ip") {
					if s, ok := val.(string); ok && s != "" && isValidIP(s) {
						ueipSet[s] = true
					}
				}
				if strings.Contains(keyLower, "teid") || strings.Contains(keyLower, "f-teid") {
					if s, ok := val.(string); ok && s != "" {
						teidSet[s] = true
					}
				}
				if strings.Contains(keyLower, "seid") || strings.Contains(keyLower, "f-seid") {
					if s, ok := val.(string); ok && s != "" {
						seidSet[s] = true
					}
				}
				if strings.Contains(keyLower, "qfi") {
					if s, ok := val.(string); ok && s != "" {
						qfiSet[s] = true
					}
				}
				search(val)
			}
		case []interface{}:
			for _, item := range v {
				search(item)
			}
		}
	}
	search(ies)
}

