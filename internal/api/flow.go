// buildDetailsMapping builds the idx->originNumber mapping from input packets JSON.
// This is the fallback method when LLM doesn't provide a MAPPING section.
// It assumes sequential ordering: Mermaid autonumber idx corresponds to packet array index.
// idx and frame are both 1-based (matching Mermaid autonumber which starts at 1).
// originNumber is the pcap frame.number (Wireshark frame number).
func buildDetailsMapping(compactJSON string) []FlowMessageDetail {
	var packets []map[string]interface{}
	if err := json.Unmarshal([]byte(compactJSON), &packets); err != nil {
		return nil
	}

	details := make([]FlowMessageDetail, 0, len(packets))
	for i, pkt := range packets {
		detail := FlowMessageDetail{
			Idx:   i + 1, // 1-based index matching Mermaid autonumber
			Frame: i + 1, // 1-based position in input array
		}

		// Extract frame.number as originNumber
		if frame, ok := pkt["frame"].(map[string]interface{}); ok {
			if num, ok := frame["number"].(string); ok {
				detail.OriginNumber = num
			}
		}

		details = append(details, detail)
	}

	return details
}

// countHeartbeatMessages counts PFCP Heartbeat messages in the packet JSON.
func countHeartbeatMessages(compactJSON string) int {
	var packets []map[string]interface{}
	if err := json.Unmarshal([]byte(compactJSON), &packets); err != nil {
		return 0
	}

	count := 0
	for _, pkt := range packets {
		if app, ok := pkt["application"].(map[string]interface{}); ok {
			if pfcp, ok := app["pfcp"].(map[string]interface{}); ok {
				// Check message field for Heartbeat
				if msg, ok := pfcp["message"].(string); ok {
					if strings.Contains(strings.ToLower(msg), "heartbeat") {
						count++
						continue
					}
				}
				// Check message_type field (1=Heartbeat Request, 2=Heartbeat Response)
				if msgType, ok := pfcp["message_type"].(string); ok {
					if msgType == "1" || msgType == "2" {
						count++
					}
				}
			}
		}
	}
	return count
}

// filterHeartbeatPacketsJSON removes PFCP heartbeat packets from the JSON array.
// This is important for absolute consistency: what we send to the LLM must be the same
// thing we later correlate in the web UI (and we don't want the model to decide "what is heartbeat").
func filterHeartbeatPacketsJSON(compactJSON string) (string, int, error) {
	var packets []map[string]interface{}
	if err := json.Unmarshal([]byte(compactJSON), &packets); err != nil {
		return compactJSON, 0, err
	}

	isHeartbeat := func(pkt map[string]interface{}) bool {
		app, ok := pkt["application"].(map[string]interface{})
		if !ok {
			return false
		}
		pfcp, ok := app["pfcp"].(map[string]interface{})
		if !ok {
			return false
		}
		if msg, ok := pfcp["message"].(string); ok {
			if strings.Contains(strings.ToLower(msg), "heartbeat") {
				return true
			}
		}
		if msgType, ok := pfcp["message_type"].(string); ok {
			if msgType == "1" || msgType == "2" {
				return true
			}
		}
		return false
	}

	filtered := make([]map[string]interface{}, 0, len(packets))
	removed := 0
	for _, pkt := range packets {
		if isHeartbeat(pkt) {
			removed++
			continue
		}
		filtered = append(filtered, pkt)
	}

	out, err := json.Marshal(filtered)
	if err != nil {
		return compactJSON, 0, err
	}
	return string(out), removed, nil
}

// countMermaidMessages counts Mermaid sequenceDiagram message arrows in a best-effort way.
func countMermaidMessages(mermaid string) int {
	if strings.TrimSpace(mermaid) == "" {
		return 0
	}
	count := 0
	lines := strings.Split(mermaid, "\n")
	for _, raw := range lines {
		line := strings.TrimSpace(raw)
		if line == "" {
			continue
		}
		if strings.HasPrefix(line, "%%") {
			continue
		}
		if strings.HasPrefix(line, "Note ") {
			continue
		}
		// A message line should contain ":" and an arrow.
		if !strings.Contains(line, ":") {
			continue
		}
		if strings.Contains(line, "->>") || strings.Contains(line, "-->>") || strings.Contains(line, "->") || strings.Contains(line, "-->") {
			count++
		}
	}
	return count
}


func safeOneLine(s string, max int) string {
	out := strings.TrimSpace(strings.ReplaceAll(s, "\n", " "))
	out = strings.Join(strings.Fields(out), " ")
	if max > 0 && len(out) > max {
		return out[:max-3] + "..."
	}
	return out
}

func getString(m map[string]interface{}, key string) string {
	if v, ok := m[key]; ok {
		if s, ok := v.(string); ok {
			return s
		}
	}
	return ""
}

